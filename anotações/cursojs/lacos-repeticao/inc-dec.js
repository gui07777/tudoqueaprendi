// colocando ++ como sufixo, isto é, depois da variável, acontece uma pós-incrementação
// ou seja, primeiro acontece a condição e assim a incrementação, enquanto, se colocar como prefixo,
// isto é, antes da variável, ocorre primeiro a incrementação e assim a condição

//mesma aplicação para decrementação


var posIncrementacao = 5;
alert(posIncrementacao++)
alert(posIncrementacao)

var preIncrementacao = 5;
alert(++preIncrementacao)
alert(preIncrementacao)

var posDecrementar =  10;
alert(posDecrementar--)
alert(posDecrementar)

var preDecrementacao = 10;
alert(--preDecrementacao)
alert(preDecrementacao)