var contador = 1;

while(contador <= 15){
    alert("Contador:" +contador);
    contador++;
}

//var contador = 1: variável contador que inicia com 1.

//while diferente do for só precisa de um parâmetro, que é a condição verdadeira.

//while(contador <= 15): enquanto o contador for menor ou igual a 15 (enquanto esta condição for verdadeira), o loop vai continuar ocorrendo. Quando chegar em 15 quebra.

//{alert("Contador" +contador)}: exiba na tela o loop.

//contador++: incrementa. Sem isso, ele ia contar apenas (1,1,1,1...), mas a incrementação avança posição, fazendo ele contar até 15.